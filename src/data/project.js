// Fungsi helper untuk filtering
export const filterProjects = (projects, filter) => {
  if (filter === "all") {
    return projects;
  }
  return projects.filter((project) => project.category === filter);
};

// Fungsi untuk mendapatkan jumlah proyek per kategori
export const getProjectCounts = (projects) => {
  const counts = {
    all: projects.length,
    web: projects.filter((p) => p.category === "web").length,
    data: projects.filter((p) => p.category === "data").length,
  };
  return counts;
};
